# Calculo del area de un triangulo
# Triangulo 1:

base = 10
altura = 5

area = (base * altura) / 2

print("Triángulo 1: ")
print("La base es: ", base)
print("La altura es: ", altura)
print("Por ende, el área del triángulo es: ", area)

